
def fordit(n):
    # 1983 -> 3891 (int)
    # 1983 -> "1983" -> "3891" -> 3891
    return int(str(n)[::-1])

def main():
    n = 1983
    print("{0} <-> {1}".format(n, fordit(n)))

##############################################################################

if __name__ == "__main__":
    main()
